void DrawNum_16x24(int x, int y, String f) {
  if (f.charAt(0) == '-')
  {
    display.drawBox(x, y + 12, 6, 3);
  }
  int p = 7;
  char c;
  for (int i = 2; i < 8; i++) {
    c = f.charAt(i);
    if (c == '.')
      display.drawBox(x + p, y + 20, 4, 4);
    else if (c <= '9' && c >= '0')
      display.drawXBM(x + p, y, 16, 24, bitmap_num_16x24[c - '0']);
    p += (c == '.') ? 6 : 18;
  }
}

void DrawNum_10x16(int x, int y, String f) {
  if (f.charAt(0) == '-')
  {
    display.drawBox(x, y + 6, 4, 2);
  }
  int p = 5;
  char c;
  for (int i = 2; i < 8; i++) {
    c = f.charAt(i);
    if (c == '.')
      display.drawBox(x + p, y + 14, 3, 3);
    else if (c <= '9' && c >= '0')
      display.drawXBM(x + p, y, 10, 16, bitmap_num_10x16[c - '0']);
    p += (c == '.') ? 5 : 12;
  }
}

