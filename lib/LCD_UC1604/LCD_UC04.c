//写指令到LCD模块
void transfer_command_lcd(uchar data1)   
{
	uchar i;
	lcd_cs1=0;
	lcd_rs=0;
	for(i=0;i<8;i++)
   {
		lcd_sclk=0;
		if(data1&0x80) lcd_sid=1;
		else lcd_sid=0;
		lcd_sclk=1;
	 	data1<<=1;
   }
	lcd_cs1=1;
}

//写数据到LCD模块
void transfer_data_lcd(uchar data1)
{
	uchar i;
	lcd_cs1=0;
	lcd_rs=1;
	for(i=0;i<8;i++)
   {
		lcd_sclk=0;
		if(data1&0x80) lcd_sid=1;
		else lcd_sid=0;
		lcd_sclk=1;
	 	data1<<=1;
    }
	lcd_cs1=1;
}

//LCD 模块初始化
void initial_lcd()
{
 lcd_reset=0; //低电平复位
 delay_ms(100);
 lcd_reset=1; //复位完毕
 delay_ms(100);
 transfer_command_lcd(0xe2); //软复位
 delay_ms(500); 
 transfer_command_lcd(0x2f); //打开内部升压
 delay_ms(500);
 transfer_command_lcd(0x81); //微调对比度
 transfer_command_lcd(0x45); //微调对比度的值，可设置范围 0x00～0xFF
 transfer_command_lcd(0xeb); //1/9 偏压比（bias）
 transfer_command_lcd(0xc4); //行扫描顺序：从上到下 oxc2
 transfer_command_lcd(0xa0); //列扫描顺序：从左到右
 transfer_command_lcd(0xaf); //开显示 
}
//设置液晶屏的显示地址（page，column）
void lcd_address(uchar page,uchar column)
{
	column=column-1;  							//
	page=page-1;
	transfer_command_lcd(0xb0+page);   				//设置页地址。每页是8行。一个画面的64行被分成8个页。我们平常所说的第1页，在LCD驱动IC里是第0页，所以在这里减去1
	transfer_command_lcd(((column>>4)&0x0f)+0x10);	//设置列地址的高4位
	transfer_command_lcd(column&0x0f);				//设置列地址的低4位
}

//全屏清屏
void clear_screen()
{
	uchar i,j;
 	for(i=0;i<9;i++)
	{
		lcd_address(1+i,1);
		for(j=0;j<192;j++)
		{
			transfer_data_lcd(0x00);
		}
	}
}
//一些出厂电测用的画面，比如全显，隔行显示，隔列显示等等
void full_screen_test(uchar data1,uchar data2)
{
	uchar i,j;
 	for(i=0;i<9;i++)
	{
		lcd_address(1+i,1);
		for(j=0;j<96;j++)
		{
			transfer_data_lcd(data1);
			transfer_data_lcd(data2);
		}
	}
}

